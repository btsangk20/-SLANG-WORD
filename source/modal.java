public class modal {
  public int id;
  public String slang;
  public String definition;

  void setId(int _id) {
    id = _id;
  }

  void setSlang(String _slang) {
    slang = _slang;
  }
  void setDefinition(String _definition) {
    definition = _definition;
  }

  int getId() {
    return id;
  }

  String getSlang() {
    return slang;
  }

  String getDefinition() {
    return definition;
  }
}
